package week1task;

public class SecondUser {
	private String name;
	private int age;
	private String phNo;
	
	public SecondUser(String name, int age, String phNo) {
		this.name = name;
		this.age = age;
		this.phNo = phNo;
	}
	
	public String getName() {
		return name;
	}
	
	public int getAge() {
		return age;
	}
	
	public String getPhNo() {
		return phNo;
	}
}
